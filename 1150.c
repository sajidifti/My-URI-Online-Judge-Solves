#include <stdio.h>
int main()
{
    int a,b,c=0,x,z;
    scanf("%d", &x);
    while(1)
    {
        if(x>=z)
        {
            scanf("%d", &z);
        for(a=x,b=0; b<z; a++)
    {
        b+=a;
        c++;
    }
        }

        else break;

    }

    printf("%d\n", c);
    return 0;
}
